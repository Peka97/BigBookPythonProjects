bitmap ="""
....................................................................
   **************   *  *** **  *      ******************************
  ********************* ** ** *  * ****************************** *
 **      *****************       ******************************
          *************          **  * **** ** ************** *
           *********            *******   **************** * *
            ********           ***************************  *
   *        * **** ***         *************** ******  ** *
               ****  *         ***************   *** ***  *
                 ******         *************    **   **  *
                 ********        *************    *  ** ***
                   ********         ********          * *** ****
                   *********         ******  *        **** ** * **
                   *********         ****** * *           *** *   *
                     ******          ***** **             *****   *
                     *****            **** *            ********
                    *****             ****              *********
                    ****              **                 *******   *
                    ***                                       *    *
                    **     *                    *
...................................................................."""

symbol = input("Введите фразу или символ: ")


def new_map(sym):
	if len(sym) == 1:
		for line in bitmap.split("\n"):
			print(line.replace('*', sym))
	else:
		for line in bitmap.split("\n"):
			for idx, el in enumerate(line):
				if el == "*":
					print(sym[idx % len(sym)], end='')
				else:
					print(' ', end='')
			print()

new_map(symbol)



